package org.mifosplatform.organisation.exchangerate.handler;

import org.mifosplatform.commands.annotation.CommandType;
import org.mifosplatform.commands.handler.NewCommandSourceHandler;
import org.mifosplatform.infrastructure.core.api.JsonCommand;
import org.mifosplatform.infrastructure.core.data.CommandProcessingResult;
import org.mifosplatform.organisation.exchangerate.service.ExchangeRateWritePlatformService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

@Service
@CommandType(entity = "EXCHANGERATE", action = "UPDATE")
public class UpdateExchangeRateCommandHandler implements NewCommandSourceHandler {

    private final ExchangeRateWritePlatformService writePlatformService;

    @Autowired
    public UpdateExchangeRateCommandHandler(final ExchangeRateWritePlatformService writePlatformService) {
        this.writePlatformService = writePlatformService;
    }

    @Transactional
    @Override
    public CommandProcessingResult processCommand(final JsonCommand command) {

        return this.writePlatformService.updateExchangeRate(command.entityId(), command);
    }
}